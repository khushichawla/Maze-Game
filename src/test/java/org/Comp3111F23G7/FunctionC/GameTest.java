    @Test
    public void testIsValidMove() {
        int[][] maze = {
                {1, 0, 0},
                {0, 0, 0},
                {0, 0, 1}
        };
        int[] validMove = {1, 1};
        assertTrue(Game.isValidMove(validMove, maze));
        int[] outOfBoundsMove = {2, 2};
        assertFalse(Game.isValidMove(outOfBoundsMove, maze));
        int[] wallMove = {0, 0};
        assertFalse(Game.isValidMove(wallMove, maze));
    }
